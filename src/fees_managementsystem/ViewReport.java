 public void setRecordToTable() throws SQLException
 {
      String url="jdbc:mysql://localhost:3306/database";
        String uname="root";
        String pwd="root";
        String courseCombo=comboCourse.getSelectedItem().toString();
        
        SimpleDateFormat date=new SimpleDateFormat("YYYY-MM-dd");
        String Fromdate=date.format(DateFrom.getDate());
        String ToDate=date.format(DateTo.getDate());
        int totalAmount=0;
        
        Connection con=DriverManager.getConnection(url,uname,pwd);
        PreparedStatement ps=con.prepareStatement("select * from feesdetail where date between ? and ? and courses =?");
        ps.setString(1, Fromdate);
        ps.setString(2, ToDate);
        ps.setString(3, courseCombo);
        ResultSet rs= ps.executeQuery();
        while(rs.next())
        {
            String Reciept=rs.getString("RecieptNo");
            String Name=rs.getString("StudentName");
            String Course=rs.getString("courses");
            int amount=rs.getInt("amount");
            String Remark=rs.getString("remark");
            totalAmount=totalAmount+amount;
            
           
            
            Object[] obj={Reciept,Name,Course,amount,Remark};
         model = (DefaultTableModel)FeesTable.getModel();
            model.addRow(obj);
 }
        String amount=Integer.toString(totalAmount);
        lbl_totalAmount.setText(amount);
        lbl_course.setText(courseCombo);
        ansWords.setText(NumberToWordsConverter.convert(totalAmount));
 }
